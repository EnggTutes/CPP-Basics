#include <iostream>
using namespace std;

int main()
{
    int a = 65, b = 93;

    cout << "\n Logical AND operator a & b = " << (a & b);
    cout << "\n Logical OR operator a | b = " << (a | b);
    cout << "\n Logical XOR operator a ^ b = " << (a ^ b);
    cout << "\n Left Shift Operator a << b = " << (a << b);
    cout << "\n Right Shift Operator a >> b = " << (a >> b);
    cout << "\n Logical NOT operator !a = " << (!a);
}