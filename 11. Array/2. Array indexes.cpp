#include <iostream>
using namespace std;

int main()
{
    int a[4] = {1, 100, 3, 4};

    cout << "\n a[0] = " << a[0];
    cout << "\n a[1] = " << a[1];
    cout << "\n a[2] = " << a[2];
    cout << "\n a[3] = " << a[3];

    cout << "\n ====================================================================";

    int i = 2;
    cout << "\n a[i] = " << a[i];
    cout << "\n i[a] = " << i[a];

    cout << "\n ====================================================================";

    cout << "\n a[-1] = " << a[-1];
    cout << "\n a[-2] = " << a[-2];
    cout << "\n a[10] = " << a[10];

    cout << "\n ====================================================================";

    cout << "\n a = " << a;
    cout << "\n &a = " << &a;
    cout << "\n &a[0] = " << &a[0];
    cout << "\n &a[1] = " << &a[1];
    cout << "\n &a[2] = " << &a[2];
    cout << "\n &a[3] = " << &a[3];

    cout << "\n ====================================================================";

    cout << "\n Number of elements in the array: " << sizeof(a) / sizeof(a[0]);

    cout << "\n ====================================================================";

    i = 1;
    cout << "\n a[i + 1] = " << a[i + 1];
    cout << "\n a[i] + 1 = " << a[i] + 1;
    cout << "\n a + 1 = " << a + 1;
    cout << "\n a - 1 = " << a - 1;
}